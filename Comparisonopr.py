# Comparison Operators
a = 10
b = 3

print("\nComparison Operators:")

# Checks if both variables are equal to each other
print("Equal:", a == b)

# Checks if both variables are not equal to each other
print("Not Equal:", a != b)

# Checks if one  variables is grater then  other
print("Greater than:", a > b)

# Checks if one  variables is less then  other
print("Less than:", a < b)

# Checks if one  variables is grater then equal to other
print("Greater than or equal to:", a >= b)

# Checks if one  variables is less then equal to other
print("Less than or equal to:", a <= b)
