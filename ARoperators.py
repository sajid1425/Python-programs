# Arithmetic Operators
a = 10
b = 3
print("Arithmetic Operators:")

# Addition
c=a+b
print("Addition:", c)

# Subtraction
c=a-b
print("Subtraction:", c)

# Multiplication
c=a*b
print("Multiplication:", c)

# Division
c=a/b
print("Division:", c)

# Modulus
c=a%b
print("Modulus:", c)

# Exponentiation
c=a**b
print("Exponentiation:", c)

# Floor Division
c=a//b
print("Floor Division:", c)