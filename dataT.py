# Integer
a = 10
print("Integer:", a)
print(type(a))

# Float
a = 20.5
print("\nFloat:", a)
print(type(a))

# Complex
a = 3 + 4j
print("\nComplex:", a)
print(type(a))

# String
a = "Hello, Python!"
print("\nString:", a)
print(type(a))

# List
a = [1, 2, 3, 4, 5]
print("\nList:", a)
print(type(a))

# Tuple
a = (10, 20, 30)
print("\nTuple:", a)
print(type(a))

# Dictionary
a = {"name": "Alice", "age": 25}
print("\nDictionary:", a)
print(type(a))
print("Dictionary (access 'name'):", a["name"])

# Set
a = {1, 2, 3, 4, 5}
print("\nSet:", a)
print(type(a))

# Boolean
a = True
print("\nBoolean:", a)
print(type(a))

# Frozenset
a = frozenset([1, 2, 3, 4, 5])
print("\nFrozenset:", a)
print(type(a))